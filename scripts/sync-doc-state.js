#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const mode = process.argv.includes('--check') ? 'check' : 'write';

function read(rel) {
  return fs.readFileSync(path.join(repoRoot, rel), 'utf8');
}

function exists(rel) {
  return fs.existsSync(path.join(repoRoot, rel));
}

function write(rel, content) {
  fs.writeFileSync(path.join(repoRoot, rel), content, 'utf8');
}

function requireMatch(text, regex, label) {
  const match = text.match(regex);
  if (!match) throw new Error(`Could not find ${label}`);
  return match;
}

const dataJs = read('js/data.js');
const saveJs = read('js/save.js');
const indexHtml = read('index.html');
const stylesCss = read('css/styles.css');
const uiControlsJs = read('js/ui-controls.js');
const prePush = exists('.githooks/pre-push') ? read('.githooks/pre-push') : '';
const preCommit = exists('.githooks/pre-commit') ? read('.githooks/pre-commit') : '';

const rosterBlock = requireMatch(dataJs, /const COMPANION_ROSTER = \{([\s\S]*?)\n\};/, 'COMPANION_ROSTER block')[1];
const classBlock = requireMatch(dataJs, /const COMPANION_CLASS_PROFILES = \{([\s\S]*?)\n\};/, 'COMPANION_CLASS_PROFILES block')[1];
const aiBlock = requireMatch(dataJs, /const COMPANION_AI_MODES = \{([\s\S]*?)\n\};/, 'COMPANION_AI_MODES block')[1];
const minimapBlock = requireMatch(stylesCss, /#minimap-container\s*\{([\s\S]*?)\n\}/, '#minimap-container block')[1];

const rosterCount = [...rosterBlock.matchAll(/^\s+\d+:/gm)].length;
const classNames = [...classBlock.matchAll(/className:'([^']+)'/g)].map(m => m[1]);
const aiModes = [...aiBlock.matchAll(/^\s+([a-z]+):\s*\{/gm)].map(m => m[1]);
const hudActions = [...indexHtml.matchAll(/class="hud-quick-btn"\s+data-action="([^"]+)"/g)].map(m => m[1]);
const minimapTop = (minimapBlock.match(/top:\s*([^;]+);/) || [null, 'unknown'])[1].trim();
const minimapRight = (minimapBlock.match(/right:\s*([^;]+);/) || [null, 'unknown'])[1].trim();
const stateLoaded = /<script src="js\/state\.js\?v=/.test(indexHtml);
const aiSaveLoad = /companionAIModes:\s*\{/.test(saveJs) && /data\.companionAIModes/.test(saveJs);
const minimapTogglePersist = /localStorage\.getItem\('rpg_minimap_visible'\)/.test(uiControlsJs);
const bootHook = /node scripts\/check-boot\.js/.test(prePush);
const docSyncHook = /node scripts\/sync-doc-state\.js --write/.test(preCommit);
const docSyncGuard = /node scripts\/sync-doc-state\.js --check/.test(prePush);

const snapshotBullets = [
  '- Authoritative implemented status lives in `docs/implemented-state.md` (auto-generated).',
  stateLoaded
    ? '- 런타임 가변 상태는 `state.js`로 분리되어 있고 `index.html`에서 직접 로드된다.'
    : '- 런타임 상태 분리 여부를 자동 감지하지 못했다.',
  `- 동료 시스템은 ${rosterCount}명 roster / ${classNames.length}개 병종 프로필 구조로 감지됐다: ${classNames.join(', ')}.`,
  `- 동료 AI 모드는 ${aiModes.map(m => `\`${m}\``).join(', ')} 로 감지됐다${aiSaveLoad ? '고 save/load 연동도 확인됐다' : '지만 save/load 연동은 확인되지 않았다'}.`,
  `- 미니맵 컨테이너는 HUD 우측 상단 슬롯(top ${minimapTop}, right ${minimapRight})으로 감지됐다${minimapTogglePersist ? '고 표시 상태는 localStorage에 저장된다' : ''}.`,
  `- HUD quick action 버튼은 ${hudActions.length}개 감지됐다: ${hudActions.map(a => `\`${a}\``).join(', ')}.`,
  bootHook
    ? '- pre-push 훅은 `scripts/check-boot.js`를 실행해 부팅 스모크 테스트를 강제한다.'
    : '- pre-push 훅에서 부팅 스모크 테스트를 감지하지 못했다.',
  docSyncHook
    ? '- pre-commit 훅은 `scripts/sync-doc-state.js --write`를 실행해 문서 스냅샷을 자동 갱신한다.'
    : '- pre-commit 자동 문서 동기화 훅은 아직 감지되지 않았다.',
  docSyncGuard
    ? '- pre-push 훅은 문서 스냅샷 sync 여부도 검사한다.'
    : '- pre-push 문서 sync guard는 아직 감지되지 않았다.',
];

const snapshotBlock = [
  '<!-- AUTO-SNAPSHOT:START -->',
  ...snapshotBullets,
  '<!-- AUTO-SNAPSHOT:END -->',
].join('\n');

const implementedStateDoc = [
  '# Implemented State Snapshot',
  '',
  '> This file is auto-generated by `node scripts/sync-doc-state.js`. Do not edit manually.',
  '',
  '## Runtime / Structure',
  '',
  stateLoaded
    ? '- [x] `state.js` runtime state module is present and loaded by `index.html`.'
    : '- [ ] `state.js` runtime state module could not be confirmed from `index.html`.',
  '- [x] `data.js` remains the authoritative source for immutable game data tables.',
  '',
  '## Companion System',
  '',
  `- [x] Companion roster entries detected: **${rosterCount}**.`,
  `- [x] Companion class profiles detected: **${classNames.length}** (${classNames.join(', ')}).`,
  `- [x] AI modes detected: ${aiModes.map(m => `\`${m}\``).join(', ')}.`,
  aiSaveLoad
    ? '- [x] AI mode save/load persistence detected in `js/save.js`.'
    : '- [ ] AI mode save/load persistence was not detected in `js/save.js`.',
  '',
  '## HUD / UX',
  '',
  `- [x] Minimap HUD slot detected at **top ${minimapTop} / right ${minimapRight}**.`,
  minimapTogglePersist
    ? '- [x] Minimap visibility persistence detected via `localStorage`.'
    : '- [ ] Minimap visibility persistence was not detected.',
  `- [x] HUD quick actions detected: ${hudActions.map(a => `\`${a}\``).join(', ')}.`,
  '',
  '## Automation',
  '',
  docSyncHook
    ? '- [x] pre-commit auto doc sync hook enabled.'
    : '- [ ] pre-commit auto doc sync hook not detected.',
  docSyncGuard
    ? '- [x] pre-push doc sync guard enabled.'
    : '- [ ] pre-push doc sync guard not detected.',
  bootHook
    ? '- [x] pre-push boot smoke test hook enabled.'
    : '- [ ] pre-push boot smoke test hook not detected.',
  '',
  '## Source Inputs',
  '',
  '- `index.html`',
  '- `css/styles.css`',
  '- `js/data.js`',
  '- `js/save.js`',
  '- `js/ui-controls.js`',
  '- `.githooks/pre-commit`',
  '- `.githooks/pre-push`',
  '',
].join('\n');

function syncSnapshotDoc(relPath) {
  const original = read(relPath);
  const next = /<!-- AUTO-SNAPSHOT:START -->[\s\S]*<!-- AUTO-SNAPSHOT:END -->/.test(original)
    ? original.replace(/<!-- AUTO-SNAPSHOT:START -->[\s\S]*<!-- AUTO-SNAPSHOT:END -->/, snapshotBlock)
    : original.replace(
        /### Current Implemented Snapshot(?:[^\n]*)\n\n[\s\S]*?\n---\n/,
        `### Current Implemented Snapshot (auto-synced)\n\n${snapshotBlock}\n\n---\n`
      );
  return { original, next };
}

const updates = [
  { rel: 'docs/implemented-state.md', content: implementedStateDoc },
  ...['AGENTS.md', 'CLAUDE.md'].map(rel => {
    const synced = syncSnapshotDoc(rel);
    return { rel, content: synced.next, original: synced.original };
  }),
];

const changed = [];
for (const update of updates) {
  const current = Object.prototype.hasOwnProperty.call(update, 'original') ? update.original : (exists(update.rel) ? read(update.rel) : '');
  if (current !== update.content) changed.push(update.rel);
  if (mode !== 'check' && current !== update.content) write(update.rel, update.content);
}

if (mode === 'check' && changed.length) {
  console.error('[doc-sync] Out-of-sync files detected:');
  for (const rel of changed) console.error(` - ${rel}`);
  console.error('[doc-sync] Run: node scripts/sync-doc-state.js --write');
  process.exit(1);
}

if (mode !== 'check') {
  if (changed.length) {
    console.log(`[doc-sync] Updated ${changed.length} file(s): ${changed.join(', ')}`);
  } else {
    console.log('[doc-sync] Documentation snapshot already up to date.');
  }
} else {
  console.log('[doc-sync] Documentation snapshot is in sync.');
}
